public class _1javabasic {
    public static void main(String args[]){
        System.out.print("hello world");
        // using (ln) for line changing.
        System.out.println("hello world");
        System.out.println("hello world");
        // using (\n) for line changing.
        System.out.print("hello world\n");
        System.out.print("hello world\n");
        System.out.println("\n \n");
        // pattern print
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
    }
}
