public class _2operations {
    public static void main(String args[]){
        // SUM 
        int a = 10 ;
        int b = 20 ;
        int sum = a + b ;
        System.out.println(sum);



        // Product 
        int product = a * b;
        System.out.println(product);



        // DIVISION
        int div = b / a;
        System.out.println(div);



        // SUBSTRACTION 
        int sub = b - a;
        System.out.println(sub);
    }

}
